pub const LogoInfo = struct {
    names: []const []const u8,
    colors: []const Color,

    pub fn init(names: []const []const u8, colors: []const Color) LogoInfo {
        return LogoInfo{
            .names = names,
            .colors = colors,
        };
    }

    pub fn matchNames(self: LogoInfo, name: []const u8) bool {
        for (self.names) |n| {
            if (std.mem.eql(u8, n, name)) {
                return true;
            }
        }
        return false;
    }
};

